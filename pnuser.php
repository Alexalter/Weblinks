/**
 * function view
 */
function Weblinks_user_view() // ready
{
    $dom = ZLanguage::getModuleDomain('Weblinks');

    // Security check
    if (!SecurityUtil::checkPermission('Weblinks::', "::", ACCESS_READ)) {
        return LogUtil::registerPermissionError();
    }

    // get all categories
    $categories = ModUtil::apiFunc('Weblinks', 'user', 'categories');

    // value of the function is checked
    if (!$categories) {
        return DataUtil::formatForDisplayHTML(__('No existing categories', $dom));
    }

    // create output
    $render = Zikula_View::getInstance('Weblinks', false);

    // assign various useful template variables
    $render->assign('categories', $categories);
    $render->assign('numrows', ModUtil::apiFunc('Weblinks', 'user', 'numrows'));
    $render->assign('catnum', ModUtil::apiFunc('Weblinks', 'user', 'catnum'));
    $render->assign('helper', array('main' => 1, 'tb' => ModUtil::getVar('Weblinks', 'targetblank')));
    if (ModUtil::getVar('Weblinks', 'featurebox') == 1) {
        $render->assign('linkbox', ModUtil::getVar('Weblinks', 'featurebox'));
        $render->assign('blocklast', ModUtil::apiFunc('Weblinks', 'user', 'lastweblinks'));
        $render->assign('blockmostpop', ModUtil::apiFunc('Weblinks', 'user', 'mostpopularweblinks'));
    } else {
        $render->assign('linkbox', ModUtil::getVar('Weblinks', 'featurebox'));
    }

    // return the output that has been generated by this function
    return $render->fetch('weblinks_user_view.html');
}

